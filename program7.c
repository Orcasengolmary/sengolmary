#include <stdio.h>

int main(void)
{

	int a = 99;
	float b = 199.76;
	double c = 9.55e+23;
	char d = 'W';
	
	printf ("a = %i\n", a);
	printf ("b = %f\n", b);
	printf ("c = %e\n", c);
	printf ("c = %g\n", c);
	printf ("d = %c\n", d);

	return 0;

}